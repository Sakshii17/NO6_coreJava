//Program to demonstrate on Bitwise Operators
package com.capgemini.operators;

public class BitwiseOperator {

	public static void main(String[] args) {
		int x=13,y=9;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(8>>1);
		System.out.println(8<<1);

	}

}
